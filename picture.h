// 임시 monster 클래스 (테스트용, 실제로는 monster.h를 사용하세요)
class monster {
    std::string name;
    int hp, atk, def, comp;
    std::string shape;
public:
    monster(std::string n, int h, int a, int d, int c, std::string s)
        : name(n), hp(h), atk(a), def(d), comp(c), shape(s) {
    }
    std::string getName() const { return name; }
    void printShape() const { std::cout << shape << std::endl; }
};

